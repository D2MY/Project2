    public function getCommentsForComposition(int $id)
    {
        return $this->createQueryBuilder('c')
            ->select('c, u.firstName, u.secondName')
            ->leftJoin(User::class,'u', 'WITH', 'c.user = u.id')
            ->where('c.composition = :id')
            ->setParameter('id', $id)
            ->orderBy('c.createdAt')
            ->getQuery()
            ->getResult();
    }

